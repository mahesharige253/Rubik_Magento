            'used_in_product_listing' => false,
            'unique' => false,
            'apply_to' => '',
            'group' => 'Inventory Material Master',
        ]);
        
        $eavSetup->addAttribute('catalog_product', 'channel_item_id', [
            'type' => 'varchar',
            'backend' => '',
            'frontend' => '',
